package Entity;

public class PlaceOfInterestEntity {

	private int poi_id;
	private String poi_name;
	private int poi_nearschool;
	private int poi_type;
	public int getPoi_id() {
		return poi_id;
	}
	public void setPoi_id(int poi_id) {
		this.poi_id = poi_id;
	}
	public String getPoi_name() {
		return poi_name;
	}
	public void setPoi_name(String poi_name) {
		this.poi_name = poi_name;
	}
	public int getPoi_nearschool() {
		return poi_nearschool;
	}
	public void setPoi_nearschool(int poi_nearschool) {
		this.poi_nearschool = poi_nearschool;
	}
	public int getPoi_type() {
		return poi_type;
	}
	public void setPoi_type(int poi_type) {
		this.poi_type = poi_type;
	}
}
