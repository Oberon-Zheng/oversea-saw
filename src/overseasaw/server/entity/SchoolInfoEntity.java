package overseasaw.server.entity;

public class SchoolInfoEntity {

	private int sch_id;
	private String sch_name;
	private String sch_country;
	private String sch_address;
	private String sch_tel;
	public int getSch_id() {
		return sch_id;
	}
	public void setSch_id(int sch_id) {
		this.sch_id = sch_id;
	}
	public String getSch_name() {
		return sch_name;
	}
	public void setSch_name(String sch_name) {
		this.sch_name = sch_name;
	}
	public String getSch_country() {
		return sch_country;
	}
	public void setSch_country(String sch_country) {
		this.sch_country = sch_country;
	}
	public String getSch_address() {
		return sch_address;
	}
	public void setSch_address(String sch_address) {
		this.sch_address = sch_address;
	}
	public String getSch_tel() {
		return sch_tel;
	}
	public void setSch_tel(String sch_tel) {
		this.sch_tel = sch_tel;
	}
}
